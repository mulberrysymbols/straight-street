<h2><a name="formats"></a>Symbol formats</h2>
<p>Symbols are provided as .wmf and .svg , as colour and in black and white.  They will scale from the size of a postage stamp to A4 without any loss of quality.  We are considering providing symbols in .jpg and .png and will continue to look at new formats as they emerge.</p>

<h2><a name="opensource"></a>Open Source</h2>
<p>Our charity fully supports this philosophy and our software is available as Open Source, which means the underlying code is available for examination and, indeed, improvements by other developers.</p>

<p>Each month, Reviewers take part in a 15-min exercise to evaluate the next batch of symbols.  We assess all those comments and will either make the new symbols available on our website, or sometimes send a symbol back to the drawing board.  If you like the idea of joining our team then please <a href="/contact.php?subject=Become%20Reviewer">contact us</a> and state "I'd like to become a Reviewer".</p>

