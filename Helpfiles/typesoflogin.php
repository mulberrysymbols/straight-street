<h1>Types of user login</h1>

<h2><a name="subscriber"></a>Subscriber</h2>
<p>A subscriber is the type of login you get when you first register.  It allows you to search our website and download symbols.</p>   

<h2><a name="reviewer"></a>Reviewer</h2>
<p>A Reviewer has been given extra authority at this website.  Our volunteer Reviewers tend to be
speech and language therapists,
teaching staff with a particular interest in the use of symbols in schools,
families and carers working with people with special needs.</p>

<p>Each month, Reviewers take part in a 15-min exercise to evaluate the next batch of symbols.  We assess all those comments and will either make the new symbols available on our website, or sometimes send a symbol back to the drawing board.  If you like the idea of joining our team, use the "Contact Us" button on the website and state "I'd like to become a Reviewer".</p>

<h2><a name="contributor"></a>Contributor</h2>
<p>A Contributor is someone who is helping build up our library. As well as symbols, we plan to add photos to the Straight Street website, and add sounds as well.  Sounds are often used on communication devices. 

<p>If you can use your photographic skills or are able to produce sounds (such as .wav or .mp3), and would be willing to have these distributed by us, please <a href="/contact.php?subject=Like%20to%20become%20a%20contributor">contact us</a> and state "I'd like to become a Contributor".  The Contributor is given the extra authority to upload files to our website and is able to join in discussions on our Contributor's discussion forum.</p>

<h2><a name="developer"></a>Developer</h2>

<p>A Developer is typically a computer programmer who wishes to provide free computer programs to enable people with communication difficulties or disabilities.   The Developer is given the extra authority to upload files to our website and is able to join in discussions on our Developer's discussion forum.</p>

