<div class="h3" id="registering" title="registering">
<h3>I had trouble Registering - what did I do wrong?</h3>
<p>You should receive an error message.  If this did not make sense, please <a href="/contact.php?subject=Trouble%20registering">contact us</a>.  Typical problems are:
	- you chose a UserID that already exists.
	- Spam filters at your organisation or home blocked the 
	authentication email.   Let us know and we can activate from 
	here.</p>
</div>
