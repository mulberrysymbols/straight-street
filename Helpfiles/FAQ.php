<div class="h2" id="general-questions" title="general-questions">
<h2>General questions:</h2>

<div class="h3" id="give" title="give">
<h3>Can I give the symbols away?</h3>
<p>Yes, once you have downloaded the symbols, you can copy them (to CD-Rom perhaps) and pass them on to your friends and colleagues. Remember that the license is associated with the original symbols and any copies in perpetuity.
</p>
</div>
<div class="h3" id="alter" title="alter">
<h3>Am I allowed to alter the symbols?</h3>
<p>Yes, and you can distribute those too, but Remember the license is associated these symbols in perpetuity, including any derivatives that you might make.  You might decide, for example, to change the colours on some symbols, perhaps to suit a particular user who has a colour-blindness.</p>
</div>
<div class="h3" id="privacy" title="privacy">
<h3>Who can see my personal details?</h3>
<p>No one.  We will not distribute any personal details or make them public.  The only information people can see about you is your UserID (which can be as anonymous as you like, although many people use the convention 
	<br/>XxxxxY<br/>
(where Xxxxxx is your first name and Y the initial letter of your surname). This appears in any forum discussions.  
Secondly, if you choose to, your "role" can be made public (for example "Speech and Language therapist").  See "My Info" on the main user panel, which becomes visible once you have logged in.</p>  
</div>
</div>

