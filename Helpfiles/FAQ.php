<div class="h2" id="general-questions" title="general-questions">
<h2>Developer and Contributor questions:</h2>
<div class="h3" id="developer" title="developer">
<h3>I'm a developer - can I write some apps for you?</h3>
<p>Yes, in the past we developed everything in Borland Delphi, but we will accept any programming language.  For us it is more about the suitability of your application and how it fits in with ours.  We might refer you on to the OATS website where you will find a much broader range of applications relating to accessibility.  Please note that all our application developers are volunteers, and you will need to allow your code to be distributed under the GNU GPL license.  Of course we will need to vet the application first. </p>
</div>
<div class="h3" id="webdesigner" title="webdesigner">
<h3>I am a web designer - how can I contribute?</h3>
<p>We are always looking to develop our website further. Please note that all our web site developers are volunteers, and the site is written in PHP/CSS/mySQL.   </p>
</div>
<div class="h3" id="photographer" title="photographer">
<h3>I am a photographer - can I donate some photos?</h3>
<p>Yes. We have a specification for the photos we distribute, which you can find in the User Guides section.</p>
</div>
